'''
Collect and format test results from JSON files generated by qa_e2e.py

Usage:
    python collect_json_results.py input_dir output_file [--debug] [--eval]
'''

import os
import json
import argparse
from datetime import datetime

def collect_json_results(input_dir, output_file, include_debug=False, include_eval=False):
    """
    Collect results from JSON files generated by the test script and format them into a markdown file.
    
    Args:
        input_dir (str): Directory containing the JSON result files
        output_file (str): Path to the output markdown file
        include_debug (bool): Include debug information in the output (default: False)
        include_eval (bool): Include evaluation scores in the output (default: False)
    """
    print(f"Collecting results from {input_dir}...")
    
    # Check if input directory exists
    if not os.path.exists(input_dir):
        print(f"Error: Input directory '{input_dir}' not found.")
        return
    
    # Get all JSON files in the input directory
    json_files = []
    for root, _, files in os.walk(input_dir):
        for file in files:
            if file.endswith('.json'):
                json_files.append(os.path.join(root, file))
    
    if not json_files:
        print(f"No JSON files found in '{input_dir}'.")
        return
    
    print(f"Found {len(json_files)} JSON files.")
    
    # Sort files to maintain order
    json_files.sort()
    
    # Initialize counter for questions
    question_count = 0
    
    # Write to markdown file
    with open(output_file, 'w', encoding='utf-8') as md_file:
        # Write header
        md_file.write(f"# Test Results Summary\n\n")
        md_file.write(f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n\n")
        md_file.write(f"Source directory: {input_dir}\n\n")
        md_file.write("---\n\n")
        
        # Process each JSON file
        for json_file in json_files:
            try:
                with open(json_file, 'r', encoding='utf-8') as f:
                    data = json.load(f)
                
                # Extract metadata
                # metadata = data.get('metadata', {})
                # generated_date = metadata.get('generated_date', 'Unknown')
                # database = metadata.get('database', 'Unknown')
                # batch_index = metadata.get('batch_index', 'Unknown')
                
                # Write file metadata
                # md_file.write(f"## Batch {batch_index}\n\n")
                # md_file.write(f"- **Generated:** {generated_date}\n")
                # md_file.write(f"- **Database:** {database}\n")
                # md_file.write(f"- **File:** {os.path.basename(json_file)}\n\n")
                
                # Process each question in the file
                for question_data in data.get('questions', []):
                    question_count += 1
                    
                    # Extract question data
                    original_question = question_data.get('original_question', 'N/A')
                    rewritten_question = question_data.get('rewritten_question', 'N/A')
                    need_rag = question_data.get('need_rag', 'N/A')
                    history_summary = question_data.get('history_summary', 'N/A')
                    answer = question_data.get('answer', 'N/A')
                    
                    # Write question section
                    md_file.write(f"### Question {question_count}\n\n")
                    md_file.write(f"**Question:** {original_question.strip()}\n\n")
                    
                    
                    # Include history if requested
                    if include_debug:
                        md_file.write(f"**Rewritten Question:** {rewritten_question.strip()}\n\n")
                        md_file.write(f"**Need RAG:** {need_rag}\n\n")
                        md_file.write(f"**History Summary:** {history_summary.strip()}\n\n")
                    
                        if 'rag_info' in question_data:
                            md_file.write("**RAG Info:**\n\n")
                            md_file.write("| Filename | Score | Chunk ID |\n")
                            md_file.write("|----------|-------|----------|\n")
                            
                            for rag_item in question_data['rag_info']:
                                filename = rag_item.get('filename', 'N/A')
                                score = rag_item.get('score', 'N/A')
                                chunk_id = rag_item.get('chunk_id', 'N/A')
                                md_file.write(f"| {filename} | {score} | {chunk_id} |\n")
                            
                            md_file.write("\n")
                    
                    # Write answer
                    md_file.write(f"**Answer:** {answer.strip()}\n\n")
                    
                    # Add evaluation info if available
                    if include_eval and 'score' in question_data:
                        score = question_data.get('score', 'N/A')
                        reason = question_data.get('evaluation_reason', 'N/A')
                        expected_answer = question_data.get('expected_answer', 'N/A')
                        
                        md_file.write(f"**Expected Answer:** {expected_answer.strip()}\n\n")
                        md_file.write(f"**Score:** {score}\n\n")
                        md_file.write(f"**Evaluation Reason:** {reason.strip()}\n\n")
                    
                    md_file.write("---\n\n")
                
            except Exception as e:
                print(f"Error processing file {json_file}: {str(e)}")
                continue
    
    print(f"Successfully collected results for {question_count} questions.")
    print(f"Results saved to '{output_file}'.")

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Collect and format test results from JSON files")
    parser.add_argument("input_dir", help="Directory containing JSON result files")
    parser.add_argument("output_file", help="Output markdown file path")
    parser.add_argument("--debug", action="store_true", help="Enable debug mode")
    parser.add_argument("--eval", action="store_true", help="Include evaluation scores")
    
    args = parser.parse_args()
    
    collect_json_results(args.input_dir, args.output_file, args.debug, args.eval)
